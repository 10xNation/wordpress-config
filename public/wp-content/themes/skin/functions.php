<?php


/* ============================================================================================== */
// Remove WordPress generator meta tag
/* ============================================================================================== */
add_filter('the_generator', '__return_null');
/* ============================================================================================== */


/* ============================================================================================== */
// Simplify error message for failed login attempts
/* ============================================================================================== */
function simplify_login_error_response() {
  return 'Sorry, your credentials are invalid';
}
add_filter('login_errors', 'simplify_login_error_response');
/* ============================================================================================== */


/* ============================================================================================== */
// Remove WordPress generator meta tag
/* ============================================================================================== */
add_filter('the_generator', '__return_null');
/* ============================================================================================== */


/* ============================================================================================== */
// Add a new theme folder
/* ============================================================================================== */
register_theme_directory('/var/www/wordpress/themes-dev');
/* ============================================================================================== */


/* ============================================================================================== */
// Control xmlrpc functionality: '__return_false' or '__return_true'
/* ============================================================================================== */
add_filter('xmlrpc_enabled', '__return_false');
/* ============================================================================================== */


/* ============================================================================================== */
// Disable automatic plugin updates
/* ============================================================================================== */
add_filter('auto_update_plugin', '__return_false');
/* ============================================================================================== */


/* ============================================================================================== */
// Disable automatic theme updates
/* ============================================================================================== */
add_filter('auto_update_theme', '__return_false');
/* ============================================================================================== */

